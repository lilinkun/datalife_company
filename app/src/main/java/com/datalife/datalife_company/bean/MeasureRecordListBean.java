public class MeasureRecordListBean {
    private String measureDate;
    private String measureName;
    private String measurePhone;
    private String measureError;
    private String measureInfo;

    public String getMeasureDate() {
        return measureDate;
    }

    public void setMeasureDate(String measureDate) {
        this.measureDate = measureDate;
    }

    public String getMeasureName() {
        return measureName;
    }

    public void setMeasureName(String measureName) {
        this.measureName = measureName;
    }

    public String getMeasurePhone() {
        return measurePhone;
    }

    public void setMeasurePhone(String measurePhone) {
        this.measurePhone = measurePhone;
    }

    public String getMeasureError() {
        return measureError;
    }

    public void setMeasureError(String measureError) {
        this.measureError = measureError;
    }

    public String getMeasureInfo() {
        return measureInfo;
    }

    public void setMeasureInfo(String measureInfo) {
        this.measureInfo = measureInfo;
    }
}
